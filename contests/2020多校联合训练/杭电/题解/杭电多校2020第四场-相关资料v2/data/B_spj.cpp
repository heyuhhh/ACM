using namespace std;

const int maxn = 3e5;
typedef double real;

int n;
char s[maxn + 1];
int m;
int head[maxn];

void WrongAnswer(void) {
  puts("WA");
  exit(0);
}

void Accepted(void) {
  puts("AC");
  exit(0);
}

int main(void) {
  n = fread(s, 1, maxn, stdin);
  s[n] = '\0';
  m = 0;
  for (int i = 0; i < n; ) {
    int j = i;
    while (j < n && s[j] != '\n') ++j;
    int k = j;
    while (k > i && isspace(s[k - 1])) --k;
    s[k] = '\0';
    head[m++] = i;
    i = j + 1;
  }
  while (m > 0 && s[head[m - 1]] == '\0') --m;

  if (m != T) {
    WrongAnswer();
  }
  for (int i = 0; i < T; ++i) {
    real x, y = ans[i];
    sscanf(s + head[i], "%lf", &x);
    if (!isfinite(x)) {
      WrongAnswer();
    }
    if (abs(x - y) > max(1.0, abs(y)) * eps) {
      WrongAnswer();
    }
  }
  Accepted();
}
